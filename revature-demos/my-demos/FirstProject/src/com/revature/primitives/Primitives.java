package com.revature.primitives;

public class Primitives {
	public static void main(String[] args) {
		// whole numbers
		byte b = 5;
		short s = 50;
		char c = 'b';
		int i = 100;
		long l = 20000000000000l;
		
		// decimal numbers
		float f = 32.255f;
		double d = 32.255;
		
		boolean bool = true;
		
		System.out.println(bool);
	}
}
